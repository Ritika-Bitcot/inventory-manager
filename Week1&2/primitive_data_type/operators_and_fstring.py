# Arithmetic Operators
a = 10
b = 3

print("---- Arithmetic ----")
print(f"a + b = {a + b}")
print(f"a - b = {a - b}")
print(f"a * b = {a * b}")
print(f"a / b = {a / b}")
print(f"a // b = {a // b}")
print(f"a % b = {a % b}")
print(f"a ** b = {a ** b}")

# Comparison Operators
print("---- Comparison ----")
print(f"a == b: {a == b}")
print(f"a != b: {a != b}")
print(f"a > b: {a > b}")
print(f"a < b: {a < b}")

# Logical Operators
x = True
y = False

print("---- Logical ----")
print(f"x and y: {x and y}")
print(f"x or y: {x or y}")
print(f"not x: {not x}")

# f-string formatting
user_name = "Bob"
user_age = 30

print("---- f-string Formatting ----")
print(f"Hello, my name is {user_name} and I am {user_age} years old.")
